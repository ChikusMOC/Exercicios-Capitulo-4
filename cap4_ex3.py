"""
Exercicio 3 - Peça ao usuário para digitar três valores inteiros e imprima a soma deles.
"""


print("Preciso que você digite três numeros inteiros")
num1 = int(input("Qual o primeiro? "))
num2 = int(input("Qual o segundo? "))
num3 = int(input("Qual o terceiro? "))

print(f"O somatório dos numeros informados é {num1}+{num2}+{num3} = {num1+num2+num3}")
